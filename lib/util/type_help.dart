class TypeHelp {

  static String weekToString(int week) {
    if(week==1){
      return '星期一';
    }
    if(week==2){
      return '星期二';
    }
    if(week==3){
      return '星期三';
    }
    if(week==4){
      return '星期四';
    }
    if(week==5){
      return '星期五';
    }
    if(week==6){
      return '星期六';
    }
    return '星期日';
  }
}